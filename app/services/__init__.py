# ============================================================
# 这个文件目前为空，但保留它是为了让 Python 把 services 目录识别为一个包。
#
# 什么是业务逻辑层（Service Layer）？
#   业务逻辑层是"做具体事情"的地方。
#   比如：新增学生、查询成绩、计算平均分……
#
#   接口层（api/）负责接收请求和返回响应，
#   业务逻辑层（services/）负责实际处理数据。
#
#   这样分工的好处：
#     - 接口层只关心"怎么接收和返回数据"
#     - 业务逻辑层只关心"怎么处理数据"
#     - 两边互不干扰，代码更清晰
# ============================================================
